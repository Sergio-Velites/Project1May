// ── Aplicar ────────────────────────────────────────────────────────────────
let src = readFileSync(FILE, "utf8");

let changed = 0;
const missing = [];
for (const [id, newName] of Object.entries(NAMES)) {
  const idEsc = id.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  // Buscar `id: "X",` y luego, antes del próximo `id: "...",`, la línea name.
  const idRe = new RegExp(`    id: "${idEsc}",\\n`);
  const m = idRe.exec(src);
  if (!m) {
    missing.push(id);
    continue;
  }
  // Slice from after the id line; find next `id: "...",` or end of array.
  const start = m.index + m[0].length;
  const restAfter = src.slice(start);
  const nextId = restAfter.search(/    id: "/);
  const blockEnd = nextId === -1 ? restAfter.length : nextId;
  const block = restAfter.slice(0, blockEnd);
  const nameMatch = block.match(/(    name: ")([^"]*)(",)/);
  if (!nameMatch) {
    missing.push(id);
    continue;
  }
  const before = block;
  const replaced =
    block.slice(0, nameMatch.index) +
    nameMatch[1] +
    newName +
    nameMatch[3] +
    block.slice(nameMatch.index + nameMatch[0].length);
  if (replaced !== before) {
    src = src.slice(0, start) + replaced + restAfter.slice(blockEnd);
    changed++;
  }
}
if (missing.length) console.log("No encontrados:", missing.join(", "));

writeFileSync(FILE, src);
console.log(`Aplicadas ${changed} traducciones de movimientos.`);
